        <div class="col-lg-6 col-md-offset-3">
            <h2>
                <p align="justify">
                    Evolve is a cloud computing service which provide customized Linux environment in less than a minute, easily deployed, shareable on-the-fly, and accessible and managed only through a simple web browser. 
                    <br />Flexibility, evolutivity and fiability are the key words of our project.
                    <br /><br /><br />
                    Evolve combines two technologies : LXC, LinuX Container, and Guacamole, a web appliance.
                    <br /><br />
                    LXC is a containerisation solution, a type of virtualisation that is more lightweight and fast due to the fact that it uses the host kernel for the containers. 
                    Thanks to this, the deployment time of containers is very short and their management is a lot easier. Moreover, we are hosting containers for you, so created containers are used like a new computer which doesn’t depend on your personnal machine resources .
                    <br /><br />
                    Guacamole gives you access to your containers through an HTML5 interface in your browser. No apps to download or install, and you can access it from everywhere.
                    <br /><br />
                    This association between these two elements (and a little bit of magic here and there) has given birth to EVOLVE.
                </p>
